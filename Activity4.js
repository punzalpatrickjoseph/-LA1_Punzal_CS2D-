// Step 1: Create a variable named firstFavNumb and store your favorite number
let firstFavNumb = 20;

// Step 2: Create a variable named secondFavNumb and store your second favorite number
let secondFavNumb = 10;

// Step 3: Perform the following checks

// Greater than
let greaterThan = firstFavNumb > secondFavNumb;

// Less than
let lessThan = firstFavNumb < secondFavNumb;

// Greater than or equal to
let greaterThanOrEqualTo = firstFavNumb >= secondFavNumb;

// Less than or equal to
let lessThanOrEqualTo = firstFavNumb <= secondFavNumb;

// Strict equality (===)
let strictEquality = firstFavNumb === secondFavNumb;

// Loose equality (==)
let looseEquality = firstFavNumb == secondFavNumb;

// Strict inequality (!==)
let strictInequality = firstFavNumb !== secondFavNumb;

// Loose inequality (!=)
let looseInequality = firstFavNumb != secondFavNumb;

// Step 4: Log the results of each comparison to the console
console.log("Greater than:", greaterThan);
console.log("Less than:", lessThan);
console.log("Greater than or equal to:", greaterThanOrEqualTo);
console.log("Less than or equal to:", lessThanOrEqualTo);
console.log("Strict equality (===):", strictEquality);
console.log("Loose equality (==):", looseEquality);
console.log("Strict inequality (!==):", strictInequality);
console.log("Loose inequality (!=):", looseInequality);
